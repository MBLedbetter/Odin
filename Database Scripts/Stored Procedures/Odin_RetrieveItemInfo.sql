
DROP PROCEDURE Odin_RetrieveItemInfo
GO


SET ANSI_NULLS ON
GO
SET QUOTED_IDENTIFIER ON
GO
CREATE PROCEDURE Odin_RetrieveItemInfo
	@itemId VARCHAR(18)=''
AS
BEGIN

	SET NOCOUNT ON;

	SELECT * FROM PS_MASTER_ITEM_TBL WHERE INV_ITEM_ID = @itemId AND SETID = 'SHARE'

END
GO

GRANT EXECUTE ON Odin_RetrieveItemInfo TO Odin
