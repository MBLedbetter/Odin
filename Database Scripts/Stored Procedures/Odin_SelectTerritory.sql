
DROP PROCEDURE Odin_SelectTerritory
GO

SET ANSI_NULLS ON
GO
SET QUOTED_IDENTIFIER ON
GO

CREATE PROCEDURE Odin_SelectTerritory
	@itemId VARCHAR(18)
AS
BEGIN

	SET NOCOUNT ON;

	SELECT [TERRITORY] FROM PS_ITEM_TERRITORY
	WHERE INV_ITEM_ID = @itemId

END
GO

GRANT EXECUTE ON Odin_SelectTerritory TO Odin
