
DROP PROCEDURE Odin_RetrieveOptions
GO


SET ANSI_NULLS ON
GO
SET QUOTED_IDENTIFIER ON
GO

CREATE PROCEDURE Odin_RetrieveOptions
	@optionId VARCHAR(255)
AS
BEGIN

	SET NOCOUNT ON;

	SELECT *

	FROM ODIN_OPTIONS_TABLE

	WHERE OPTION_ID = @optionId
	 
END
GO

GRANT EXECUTE ON Odin_RetrieveOptions TO Odin
GO
