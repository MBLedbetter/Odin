
DROP PROCEDURE Odin_CompareWebLicense
GO


SET ANSI_NULLS ON
GO
SET QUOTED_IDENTIFIER ON
GO

CREATE PROCEDURE Odin_CompareWebLicense
		@license VARCHAR(MAX) = ''
AS
BEGIN

	SET NOCOUNT ON;

	SELECT  LICENSE
		FROM ODIN_WEB_LICENSE 
		WHERE UPPER(LICENSE) = UPPER(@license)


END
GO

GRANT EXECUTE ON Odin_CompareWebLicense TO Odin
