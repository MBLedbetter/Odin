
DROP PROCEDURE Odin_RetrieveCustomerIdConversions
GO


SET ANSI_NULLS ON
GO
SET QUOTED_IDENTIFIER ON
GO

CREATE PROCEDURE Odin_RetrieveCustomerIdConversions
AS
BEGIN

	SET NOCOUNT ON;

	SELECT * FROM ODIN_CUSTOMER_CONVERSION ORDER BY CUST_NAME ASC

END
GO

GRANT EXECUTE ON Odin_RetrieveCustomerIdConversions TO Odin

