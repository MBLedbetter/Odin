
DROP PROCEDURE Odin_RetrieveItemWebInfo
GO


SET ANSI_NULLS ON
GO
SET QUOTED_IDENTIFIER ON
GO

CREATE PROCEDURE Odin_RetrieveItemWebInfo
	@itemId VARCHAR(18)=''
AS
BEGIN

	SET NOCOUNT ON;

	SELECT * FROM PS_ITEM_WEB_INFO WHERE INV_ITEM_ID = @itemId

END
GO

GRANT EXECUTE ON Odin_RetrieveItemWebInfo TO Odin
