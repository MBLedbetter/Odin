
DROP PROCEDURE Odin_CompareHrmnTariffCd
GO


SET ANSI_NULLS ON
GO
SET QUOTED_IDENTIFIER ON
GO

CREATE PROCEDURE Odin_CompareHrmnTariffCd
		@tariffCode VARCHAR(14)=''
AS
BEGIN

	SET NOCOUNT ON;

	SELECT * FROM PS_HRMN_TARIFF_CD
	WHERE HARMONIZED_CD = @tariffCode
	AND EFF_STATUS = 'A'

END
GO

GRANT EXECUTE ON Odin_CompareHrmnTariffCd TO Odin
