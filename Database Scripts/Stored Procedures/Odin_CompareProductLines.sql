
DROP PROCEDURE Odin_CompareProductLines
GO


SET ANSI_NULLS ON
GO
SET QUOTED_IDENTIFIER ON
GO

CREATE PROCEDURE Odin_CompareProductLines
		@prodGroup VARCHAR(30)='',
		@prodLine VARCHAR(30)=''
AS
BEGIN

	SET NOCOUNT ON;

	SELECT * FROM PS_PRODUCT_LINES
	WHERE PROD_GROUP= @prodGroup
		AND PROD_LINE = @prodLine


END
GO

GRANT EXECUTE ON Odin_CompareProductLines TO Odin
