
DROP PROCEDURE Odin_SelectLanguage
GO

SET ANSI_NULLS ON
GO
SET QUOTED_IDENTIFIER ON
GO

CREATE PROCEDURE Odin_SelectLanguage
	@itemId VARCHAR(18)
AS
BEGIN

	SET NOCOUNT ON;

	SELECT [LANGUAGE_CD] FROM PS_ITEM_LANGUAGE
	WHERE INV_ITEM_ID = @itemId

END
GO

GRANT EXECUTE ON Odin_SelectLanguage TO Odin
