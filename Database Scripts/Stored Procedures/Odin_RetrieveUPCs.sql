
DROP PROCEDURE Odin_RetrieveUPCs
GO

SET ANSI_NULLS ON
GO
SET QUOTED_IDENTIFIER ON
GO

CREATE PROCEDURE Odin_RetrieveUPCs
		@upc VARCHAR(18)=''
AS
BEGIN

	SET NOCOUNT ON;

	SELECT [INV_ITEM_ID] FROM PS_INV_ITEMS
	WHERE UPC_ID = @upc
	AND SETID = 'SHARE'

END
GO

GRANT EXECUTE ON Odin_RetrieveUPCs TO Odin

