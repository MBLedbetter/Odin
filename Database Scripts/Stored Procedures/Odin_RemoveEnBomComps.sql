
DROP PROCEDURE Odin_RemoveEnBomComps
GO


SET ANSI_NULLS ON
GO
SET QUOTED_IDENTIFIER ON
GO

CREATE PROCEDURE Odin_RemoveEnBomComps
	@itemId VARCHAR(18)= ''

AS
BEGIN

	SET NOCOUNT ON;
	
	BEGIN
		
		DELETE FROM PS_EN_BOM_COMPS 
		WHERE INV_ITEM_ID = @itemId
	END
	
END
GO

GRANT EXECUTE ON Odin_RemoveEnBomComps TO Odin
GO
