
DROP PROCEDURE Odin_CompareInvItemGroup
GO


SET ANSI_NULLS ON
GO
SET QUOTED_IDENTIFIER ON
GO

CREATE PROCEDURE Odin_CompareInvItemGroup
		@itemGroup VARCHAR(15)=''
AS
BEGIN

	SET NOCOUNT ON;

	SELECT * FROM PS_INV_ITEM_GROUP
	WHERE UPPER(INV_ITEM_GROUP) = UPPER(@itemGroup)


END
GO

GRANT EXECUTE ON Odin_CompareInvItemGroup TO Odin