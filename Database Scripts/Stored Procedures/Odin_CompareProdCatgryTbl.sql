CREATE PROCEDURE Odin_CompareProdCatgryTbl
		@prodCat VARCHAR(15) = ''
AS
BEGIN

	SET NOCOUNT ON;

	SELECT * FROM PS_PROD_CATGRY_TBL
	WHERE UPPER(PROD_CATEGORY) = UPPER(@prodCat) AND EFF_STATUS='A'


END
GO

