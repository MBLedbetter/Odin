
DROP PROCEDURE Odin_RetrieveExcelLayouts
GO


SET ANSI_NULLS ON
GO
SET QUOTED_IDENTIFIER ON
GO

CREATE PROCEDURE Odin_RetrieveExcelLayouts
AS
BEGIN

	SET NOCOUNT ON;

	SELECT *

	FROM ODIN_EXCEL_LAYOUT_IDS
	WHERE LAYOUT_NAME NOT LIKE '**%'
	ORDER BY LAYOUT_NAME ASC
	 

END
GO

GRANT EXECUTE ON Odin_RetrieveExcelLayouts TO Odin
GO
