CREATE PROCEDURE Odin_ComparePsCmGroupDefn
		@CPG VARCHAR(15)=''
AS
BEGIN

	SET NOCOUNT ON;

	SELECT * FROM PS_CM_GROUP_DEFN
	WHERE CM_GROUP= @CPG

END
GO

