CREATE PROCEDURE Odin_InsertNewDate
	@itemId CHAR(18)='',
	@newDate VARCHAR(25) =''
AS
BEGIN

	SET NOCOUNT ON;
		
	UPDATE PS_ITEM_WEB_INFO
	SET
		NEW_DATE = @newDate
	WHERE
		INV_ITEM_ID = @itemId AND NEW_DATE = ''


END
GO

GRANT EXECUTE ON Odin_InsertNewDate TO Odin
GO
