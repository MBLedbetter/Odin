
DROP PROCEDURE Odin_CompareItmCatTbl
GO


SET ANSI_NULLS ON
GO
SET QUOTED_IDENTIFIER ON
GO

CREATE PROCEDURE Odin_CompareItmCatTbl
		@itemCategory VARCHAR(15) = ''
AS
BEGIN

	SET NOCOUNT ON;

	SELECT  CATEGORY_ID
		FROM PS_ITM_CAT_TBL 
		WHERE UPPER(CATEGORY_CD) = UPPER(@itemCategory)


END
GO

GRANT EXECUTE ON Odin_CompareItmCatTbl TO Odin
