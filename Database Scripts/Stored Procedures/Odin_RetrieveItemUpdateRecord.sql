
DROP PROCEDURE Odin_RetrieveItemUpdateRecord
GO


SET ANSI_NULLS ON
GO
SET QUOTED_IDENTIFIER ON
GO

CREATE PROCEDURE Odin_RetrieveItemUpdateRecord
	@itemId VARCHAR(18)
AS
BEGIN

	SET NOCOUNT ON;

	SELECT *
	FROM ODIN_ITEM_UPDATE_RECORDS
	WHERE INV_ITEM_ID = @itemId
	ORDER BY INPUT_DATE DESC
	 
END
GO

GRANT EXECUTE ON Odin_RetrieveItemUpdateRecord TO Odin
GO
