CREATE PROCEDURE Odin_GetProductLines
		@prodGroup VARCHAR(30) = ''
AS
BEGIN

	SET NOCOUNT ON;

	SELECT DISTINCT PROD_LINE FROM PS_PRODUCT_LINES
	WHERE (@prodGroup='' or PROD_GROUP = @prodGroup)


END
GO

GRANT EXECUTE ON Odin_GetProductLines TO Odin
