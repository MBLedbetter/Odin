CREATE PROCEDURE Odin_RetrieveEcommerceUPCs
		@upc VARCHAR(18)=''
AS
BEGIN

	SET NOCOUNT ON;

	SELECT INV_ITEM_ID, UPC_OVERRIDE
	FROM PS_AMAZON_ITEM_ATTRIBUTES

END
GO

GRANT EXECUTE ON Odin_RetrieveEcommerceUPCs TO Odin
