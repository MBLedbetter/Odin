
DROP PROCEDURE Odin_GetProductFormats
GO


SET ANSI_NULLS ON
GO
SET QUOTED_IDENTIFIER ON
GO

CREATE PROCEDURE Odin_GetProductFormats
		@prodLine VARCHAR(30) = ''
AS
BEGIN

	SET NOCOUNT ON;

	SELECT DISTINCT PROD_FORMAT FROM PS_PRODUCT_FORMATS
	WHERE (@prodLine='' or PROD_LINE = @prodLine)


END
GO

GRANT EXECUTE ON Odin_GetProductFormats TO Odin
